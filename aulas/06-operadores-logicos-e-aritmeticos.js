/*======================================================================================

OPERADORES LÓGICOS

&&     "E" - As duas condições devem ser verdadeiras para que o resultado seja verdadeiro.

||     "OU" - Uma das condições deve ser verdadeira para que o resultado seja verdadeiro.

!      "NÃO" - Nega uma condição.

======================================================================================*/

console.log(5 == 5 && 6 == 6) //true
console.log(5 == 5 && 6 != 6) //false

console.log(5 == 5 || 6 == 6) //true
console.log(5 == 5 || 6 != 6) //true

console.log(!(5 > 6)) //true
console.log(!(5 < 6)) //false
//Aula 6

/*==========================

OPERADORES ARITMÉTICOS

*     Multiplicação
/     Divisão
%     Resto da divisão
+     Adição
-     Subtração

==========================*/

console.log(2 * 2) //4
console.log(2 / 2) //1
console.log(2 % 1.5) //.5
console.log(2 + 2) //4
console.log(2 - 2) //0